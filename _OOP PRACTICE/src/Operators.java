public class Operators {
    public static void main(String[] args){
        int x = 100;
        int y = 50;

        System.out.println("x + y:" + (x + y));
        System.out.println("x - y:" + (x - y));
        System.out.println("x * y:" + (x * y));
        System.out.println("x / y:" + (x / y));
        System.out.println("x % y:" + (x % y));
    }
}
